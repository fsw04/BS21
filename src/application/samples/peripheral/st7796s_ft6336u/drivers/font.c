{
    return (g_hzk16_data != NULL) && (g_hzk16_size > 0);
}

/*============================================================
 * 内部：向 LCD 写入一个像素块（支持缩放）
 *============================================================*/
static void font_write_glyph(const uint8_t *glyph, uint8_t glyph_w, uint8_t glyph_h,
                             uint16_t x, uint16_t y, uint16_t fg, uint16_t bg,
                             uint8_t scale)
{
    uint16_t pw = (uint16_t)glyph_w * scale;
    uint16_t ph = (uint16_t)glyph_h * scale;
    uint32_t total = (uint32_t)pw * ph;
    uint8_t buf[512]; /* 最大 16*16*2=512 字节 (scale=1) */
    uint16_t idx = 0;

    if ((total * 2) > sizeof(buf)) {
        return; /* 缓冲不足，安全退出 */
    }

    st7796s_set_window(x, y, x + pw - 1, y + ph - 1);
    lcd_bus_send_cmd(0x2C);

    for (uint8_t row = 0; row < glyph_h; row++) {
        uint8_t line = glyph[row];
        for (uint8_t col = 0; col < glyph_w; col++) {
            uint8_t pixel = (line >> (7 - col)) & 0x01;
            uint16_t color = pixel ? fg : bg;
            for (uint8_t sy = 0; sy < scale; sy++) {
                for (uint8_t sx = 0; sx < scale; sx++) {
                    if ((idx + 1) < (sizeof(buf) / 2)) {
                        buf[idx * 2]     = color >> 8;
                        buf[idx * 2 + 1] = color & 0xFF;
                    }
                    idx++;
                }
            }
        }
    }

    /* 分块发送 */
    uint32_t byte_len = (uint32_t)idx * 2;
    uint32_t sent = 0;
    while (sent < byte_len) {
        uint32_t chunk = byte_len - sent;
        if (chunk > 256) {
            chunk = 256;
        }
        lcd_bus_send_data_array(buf + sent, chunk);
        sent += chunk;
    }
}

/*============================================================
 * 内部：向 LCD 写入 HZK16 字符（16×16 点阵）
 *============================================================*/
static void font_write_hzk16_glyph(const uint8_t *dot_data, uint16_t x, uint16_t y,
                                   uint16_t fg, uint16_t bg, uint8_t scale)
{
    uint16_t pw = 16 * scale;
    uint16_t ph = 16 * scale;
    uint32_t total = (uint32_t)pw * ph;

    /* 缩放 1x: 16*16=256 像素, 512 字节; 缩放 2x: 1024*2=2048 字节, 需分块 */
    st7796s_set_window(x, y, x + pw - 1, y + ph - 1);
    lcd_bus_send_cmd(0x2C);

    if (scale == 1) {
        /* 1x 缩放：直接逐像素写入 */
        uint8_t buf[512];
        uint16_t idx = 0;
        for (uint8_t row = 0; row < 16; row++) {
            uint16_t line = ((uint16_t)dot_data[row * 2] << 8) | dot_data[row * 2 + 1];
            for (uint8_t col = 0; col < 16; col++) {
                uint8_t pixel = (line >> (15 - col)) & 0x01;
                uint16_t color = pixel ? fg : bg;
                buf[idx++] = color >> 8;
                buf[idx++] = color & 0xFF;
            }
        }
        lcd_bus_send_data_array(buf, idx);
    } else {
        /* 2x 缩放：逐行生成并发送，避免大缓冲 */
        uint8_t row_buf[64]; /* 16*2*2=64 字节/缩放行 */
        for (uint8_t row = 0; row < 16; row++) {
            uint16_t line = ((uint16_t)dot_data[row * 2] << 8) | dot_data[row * 2 + 1];
            for (uint8_t sy = 0; sy < scale; sy++) {
                uint16_t idx = 0;
                for (uint8_t col = 0; col < 16; col++) {
                    uint8_t pixel = (line >> (15 - col)) & 0x01;
                    uint16_t color = pixel ? fg : bg;
                    for (uint8_t sx = 0; sx < scale; sx++) {
                        row_buf[idx++] = color >> 8;
                        row_buf[idx++] = color & 0xFF;
                    }
                }
                lcd_bus_send_data_array(row_buf, idx);
            }
        }
    }
}

/*============================================================
 * 绘制 ASCII 字符
 *============================================================*/
void font_draw_ascii(uint16_t x, uint16_t y, char ch,
                     uint16_t fg, uint16_t bg, uint8_t scale)
{
    uint8_t idx = (uint8_t)ch - ASCII_FONT_OFFSET;
    if (idx >= ASCII_FONT_COUNT) {
        idx = 0; /* 非法字符显示为空格 */
    }
    font_write_glyph(g_font8x16[idx], FONT_ASCII_W, FONT_ASCII_H, x, y, fg, bg, scale);
}

/*============================================================
 * 绘制 GB2312 中文字符
 *============================================================*/
void font_draw_gb2312(uint16_t x, uint16_t y, uint8_t gb_hi, uint8_t gb_lo,
                      uint16_t fg, uint16_t bg, uint8_t scale)
{
    /* GB2312 区位码计算 */
    uint8_t qu  = gb_hi - 0xA0;
    uint8_t wei = gb_lo - 0xA0;

    if (qu < 1 || qu > 94 || wei < 1 || wei > 94) {
        /* 非法 GB2312 编码，绘制占位方块 */
        uint8_t placeholder[32];
        (void)memset_s(placeholder, sizeof(placeholder), 0xFF, sizeof(placeholder));
        font_write_hzk16_glyph(placeholder, x, y, fg, bg, scale);
        return;
    }

    if (font_hzk16_available()) {
        /* 从 HZK16 字库中查找点阵数据 */
        uint32_t offset = (uint32_t)((qu - 1) * 94 + (wei - 1)) * 32;
        if ((offset + 32) <= g_hzk16_size) {
            font_write_hzk16_glyph(g_hzk16_data + offset, x, y, fg, bg, scale);
            return;
        }
    }

    /* HZK16 不可用或偏移越界，绘制占位方块 */
    uint8_t placeholder[32];
    (void)memset_s(placeholder, sizeof(placeholder), 0xFF, sizeof(placeholder));
    font_write_hzk16_glyph(placeholder, x, y, fg, bg, scale);
}

/*============================================================
 * 绘制混合字符串（ASCII + GB2312 中文）
 *============================================================*/
uint16_t font_draw_string(uint16_t x, uint16_t y, const char *str,
                          uint16_t fg, uint16_t bg, uint8_t scale)
{
    uint16_t cur_x = x;

    if (str == NULL) {
        return cur_x;
    }

    while (*str != '\0') {
        uint8_t byte = (uint8_t)(*str);

        if (byte >= 0xA1 && byte <= 0xFE) {
            /* GB2312 中文字符（双字节） */
            uint8_t gb_hi = byte;
            str++;
            if (*str == '\0') {
                break;
            }
            uint8_t gb_lo = (uint8_t)(*str);
            str++;
            font_draw_gb2312(cur_x, y, gb_hi, gb_lo, fg, bg, scale);
            cur_x += (uint16_t)FONT_HZK16_W * scale;
        } else {
            /* ASCII 字符 */
            font_draw_ascii(cur_x, y, (char)byte, fg, bg, scale);
            cur_x += (uint16_t)FONT_ASCII_W * scale;
            str++;
        }
    }

    return cur_x;
}

/*============================================================
 * 计算字符串像素宽度
 *============================================================*/
uint16_t font_string_width(const char *str, uint8_t scale)
{
    uint16_t width = 0;

    if (str == NULL) {
        return 0;
    }

    while (*str != '\0') {
        uint8_t byte = (uint8_t)(*str);
        if (byte >= 0xA1 && byte <= 0xFE) {
            width += (uint16_t)FONT_HZK16_W * scale;
            str++;
            if (*str != '\0') {
                str++;
            }
        } else {
            width += (uint16_t)FONT_ASCII_W * scale;
            str++;
        }
    }

    return width;
}

/*============================================================
 * 获取字体行高
 *============================================================*/
uint16_t font_get_line_height(uint8_t scale)
{
    return (uint16_t)FONT_ASCII_H * scale;
}
