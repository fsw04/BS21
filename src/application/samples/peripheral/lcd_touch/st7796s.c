{
    st7796s_set_backlight(false);
    lcd_write_cmd(ST7796S_DISPOFF);
    lcd_write_cmd(ST7796S_SLPIN);
    uapi_spi_deinit(LCD_SPI_BUS_ID);
}

void st7796s_set_window(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2)
{
    lcd_write_cmd(ST7796S_CASET);
    lcd_write_data16(x1);
    lcd_write_data16(x2);
    lcd_write_cmd(ST7796S_PASET);
    lcd_write_data16(y1);
    lcd_write_data16(y2);
    lcd_write_cmd(ST7796S_RAMWR);
}

void st7796s_clear(uint16_t color)
{
    st7796s_fill(0, 0, ST7796S_WIDTH - 1, ST7796S_HEIGHT - 1, color);
}

void st7796s_fill(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color)
{
    uint32_t total = (uint32_t)(x2 - x1 + 1) * (uint32_t)(y2 - y1 + 1);
    uint8_t hi = (uint8_t)(color >> 8);
    uint8_t lo = (uint8_t)(color & 0xFF);

    st7796s_set_window(x1, y1, x2, y2);

    /* 每次发送一行数据，减少SPI调用次数 */
    uint16_t width = x2 - x1 + 1;
    uint16_t height = y2 - y1 + 1;

    /* 使用静态缓冲区，每次填充一行 */
    static uint8_t line_buf[ST7796S_WIDTH * 2]; /* 最大一行320像素 */
    uint32_t line_bytes = (uint32_t)width * 2;

    for (uint32_t i = 0; i < line_bytes; i += 2) {
        line_buf[i] = hi;
        line_buf[i + 1] = lo;
    }

    for (uint16_t row = 0; row < height; row++) {
        lcd_write_data_bulk(line_buf, line_bytes);
    }
    unused(total);
}

void st7796s_draw_pixel(uint16_t x, uint16_t y, uint16_t color)
{
    st7796s_set_window(x, y, x, y);
    lcd_write_data16(color);
}

void st7796s_draw_line(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color)
{
    int dx = (x2 > x1) ? (x2 - x1) : (x1 - x2);
    int dy = (y2 > y1) ? (y2 - y1) : (y1 - y2);
    int sx = (x1 < x2) ? 1 : -1;
    int sy = (y1 < y2) ? 1 : -1;
    int err = dx - dy;

    while (1) {
        st7796s_draw_pixel(x1, y1, color);
        if (x1 == x2 && y1 == y2) {
            break;
        }
        int e2 = 2 * err;
        if (e2 > -dy) {
            err -= dy;
            x1 += sx;
        }
        if (e2 < dx) {
            err += dx;
            y1 += sy;
        }
    }
}

void st7796s_draw_rect(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color)
{
    st7796s_draw_line(x1, y1, x2, y1, color);
    st7796s_draw_line(x1, y2, x2, y2, color);
    st7796s_draw_line(x1, y1, x1, y2, color);
    st7796s_draw_line(x2, y1, x2, y2, color);
}

void st7796s_fill_rect(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color)
{
    st7796s_fill(x1, y1, x2, y2, color);
}

void st7796s_draw_char(uint16_t x, uint16_t y, char ch, uint16_t fc, uint16_t bc, uint8_t size)
{
    if (ch < ' ' || ch > 'Z') {
        ch = ' ';
    }
    uint8_t idx = (uint8_t)(ch - ' ');
    const uint8_t *font = &g_font8x16[idx * 16];

    for (uint8_t i = 0; i < 16; i++) {
        uint8_t line = font[i];
        for (uint8_t j = 0; j < 8; j++) {
            if (line & (0x80 >> j)) {
                if (size == 1) {
                    st7796s_draw_pixel(x + j, y + i, fc);
                } else {
                    st7796s_fill_rect(x + j * size, y + i * size,
                                      x + j * size + size - 1, y + i * size + size - 1, fc);
                }
            } else {
                if (size == 1) {
                    st7796s_draw_pixel(x + j, y + i, bc);
                } else {
                    st7796s_fill_rect(x + j * size, y + i * size,
                                      x + j * size + size - 1, y + i * size + size - 1, bc);
                }
            }
        }
    }
}

void st7796s_draw_string(uint16_t x, uint16_t y, const char *str, uint16_t fc, uint16_t bc, uint8_t size)
{
    if (str == NULL) {
        return;
    }
    uint16_t x0 = x;
    while (*str != '\0') {
        if (*str == '\n') {
            x = x0;
            y += 16 * size;
            str++;
            continue;
        }
        st7796s_draw_char(x, y, *str, fc, bc, size);
        x += 8 * size;
        if (x + 8 * size > ST7796S_WIDTH) {
            x = x0;
            y += 16 * size;
        }
        str++;
    }
}

void st7796s_set_backlight(bool on)
{
    uapi_gpio_set_val(LCD_BL_PIN, on ? GPIO_LEVEL_HIGH : GPIO_LEVEL_LOW);
}
