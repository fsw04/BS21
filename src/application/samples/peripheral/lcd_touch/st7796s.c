/* DC引脚控制: DC=0写命令, DC=1写数据 */
static void lcd_dc_set(void)
{
    uapi_gpio_set_val(LCD_DC_PIN, GPIO_LEVEL_HIGH);
}

static void lcd_dc_clr(void)
{
    uapi_gpio_set_val(LCD_DC_PIN, GPIO_LEVEL_LOW);
}

/* CS引脚控制 */
static void lcd_cs_set(void)
{
    uapi_gpio_set_val(LCD_SPI_CS_PIN, GPIO_LEVEL_HIGH);
}

static void lcd_cs_clr(void)
{
    uapi_gpio_set_val(LCD_SPI_CS_PIN, GPIO_LEVEL_LOW);
}

/* RST引脚控制 */
static void lcd_rst_set(void)
{
    uapi_gpio_set_val(LCD_RST_PIN, GPIO_LEVEL_HIGH);
}

static void lcd_rst_clr(void)
{
    uapi_gpio_set_val(LCD_RST_PIN, GPIO_LEVEL_LOW);
}

/* 通过SPI写命令 */
static void lcd_write_cmd(uint8_t cmd)
{
    lcd_cs_clr();
    lcd_dc_clr();
    uint8_t tx = cmd;
    spi_xfer_data_t data = {
        .tx_buff = &tx,
        .tx_bytes = 1,
        .rx_buff = NULL,
        .rx_bytes = 0,
    };
    uapi_spi_master_write(LCD_SPI_BUS_ID, &data, 0xFFFFFFFF);
    lcd_cs_set();
}

/* 通过SPI写数据 */
static void lcd_write_data8(uint8_t data8)
{
    lcd_cs_clr();
    lcd_dc_set();
    uint8_t tx = data8;
    spi_xfer_data_t data = {
        .tx_buff = &tx,
        .tx_bytes = 1,
        .rx_buff = NULL,
        .rx_bytes = 0,
    };
    uapi_spi_master_write(LCD_SPI_BUS_ID, &data, 0xFFFFFFFF);
    lcd_cs_set();
}

/* 通过SPI写16位数据 */
static void lcd_write_data16(uint16_t data16)
{
    lcd_cs_clr();
    lcd_dc_set();
    uint8_t tx[2] = { (uint8_t)(data16 >> 8), (uint8_t)(data16 & 0xFF) };
    spi_xfer_data_t data = {
        .tx_buff = tx,
        .tx_bytes = 2,
        .rx_buff = NULL,
        .rx_bytes = 0,
    };
    uapi_spi_master_write(LCD_SPI_BUS_ID, &data, 0xFFFFFFFF);
    lcd_cs_set();
}

/* 通过SPI批量写数据(用于填充) */
static void lcd_write_data_bulk(const uint8_t *buf, uint32_t len)
{
    if (buf == NULL || len == 0) {
        return;
    }
    lcd_cs_clr();
    lcd_dc_set();
    spi_xfer_data_t data = {
        .tx_buff = (uint8_t *)buf,
        .tx_bytes = len,
        .rx_buff = NULL,
        .rx_bytes = 0,
    };
    uapi_spi_master_write(LCD_SPI_BUS_ID, &data, 0xFFFFFFFF);
    lcd_cs_set();
}

/* 初始化SPI引脚 */
static void lcd_spi_init_pin(void)
{
#if defined(CONFIG_PINCTRL_SUPPORT_IE)
    uapi_pin_set_ie(LCD_SPI_MOSI_PIN, PIN_IE_1);
#endif
    uapi_pin_set_mode(LCD_SPI_CLK_PIN, LCD_SPI_CLK_PIN_MODE);
    uapi_pin_set_mode(LCD_SPI_MOSI_PIN, LCD_SPI_MOSI_PIN_MODE);

    /* CS使用GPIO手动控制 */
    uapi_pin_set_mode(LCD_SPI_CS_PIN, HAL_PIO_FUNC_GPIO);
    uapi_gpio_set_dir(LCD_SPI_CS_PIN, GPIO_DIRECTION_OUTPUT);
    uapi_gpio_set_val(LCD_SPI_CS_PIN, GPIO_LEVEL_HIGH);

    /* DC使用GPIO */
    uapi_pin_set_mode(LCD_DC_PIN, HAL_PIO_FUNC_GPIO);
    uapi_gpio_set_dir(LCD_DC_PIN, GPIO_DIRECTION_OUTPUT);
    uapi_gpio_set_val(LCD_DC_PIN, GPIO_LEVEL_HIGH);

    /* RST使用GPIO */
    uapi_pin_set_mode(LCD_RST_PIN, HAL_PIO_FUNC_GPIO);
    uapi_gpio_set_dir(LCD_RST_PIN, GPIO_DIRECTION_OUTPUT);
    uapi_gpio_set_val(LCD_RST_PIN, GPIO_LEVEL_HIGH);

    /* 背光使用GPIO */
    uapi_pin_set_mode(LCD_BL_PIN, HAL_PIO_FUNC_GPIO);
    uapi_gpio_set_dir(LCD_BL_PIN, GPIO_DIRECTION_OUTPUT);
    uapi_gpio_set_val(LCD_BL_PIN, GPIO_LEVEL_HIGH);
}

/* 初始化SPI主机 */
static void lcd_spi_init_config(void)
{
    spi_attr_t config = { 0 };
    spi_extra_attr_t ext_config = { 0 };

    config.is_slave = false;
    config.slave_num = SPI_SLAVE0;
    config.bus_clk = LCD_SPI_BUS_CLK;
    config.freq_mhz = LCD_SPI_FREQ_MHZ;
    config.clk_polarity = 0;  /* CPOL=0 */
    config.clk_phase = 0;     /* CPHA=0 */
    config.frame_format = 0;  /* 标准SPI */
    config.spi_frame_format = HAL_SPI_FRAME_FORMAT_STANDARD;
    config.frame_size = 0x07; /* 8bit */
    config.tmod = 0;          /* 发送和接收模式 */
    config.sste = 0;

    ext_config.qspi_param.wait_cycles = 0x10;

    uapi_spi_init(LCD_SPI_BUS_ID, &config, &ext_config);
}

/* ST7796S初始化序列(参考ST7796_TN_Code.txt) */
static void lcd_init_sequence(void)
{
    /* 硬件复位 */
    lcd_rst_set();
    uapi_tcxo_delay_ms(1);
    lcd_rst_clr();
    uapi_tcxo_delay_ms(10);
    lcd_rst_set();
    uapi_tcxo_delay_ms(120);

    /* Sleep Out */
    lcd_write_cmd(ST7796S_SLPOUT);
    uapi_tcxo_delay_ms(120);

    /* 解锁命令集 */
    lcd_write_cmd(0xF0);
    lcd_write_data8(0xC3);
    lcd_write_cmd(0xF0);
    lcd_write_data8(0x96);

    /* MADCTL: Memory Data Access Control */
    lcd_write_cmd(ST7796S_MADCTL);
    lcd_write_data8(0x48); /* RGB, 行顺序, 列顺序 */

    /* 像素格式: 16bit/pixel (RGB565) */
    lcd_write_cmd(ST7796S_PIXFMT);
    lcd_write_data8(0x55);

    /* Inversion Control */
    lcd_write_cmd(ST7796S_INVCTR);
    lcd_write_data8(0x01);

    /* Display Inversion Control */
    lcd_write_cmd(0xB7);
    lcd_write_data8(0xC6);

    /* Frame Rate Control */
    lcd_write_cmd(0xE8);
    lcd_write_data8(0x40);
    lcd_write_data8(0x8A);
    lcd_write_data8(0x00);
    lcd_write_data8(0x00);
    lcd_write_data8(0x29);
    lcd_write_data8(0x19);
    lcd_write_data8(0xA5);
    lcd_write_data8(0x33);

    /* Power Control 2 */
    lcd_write_cmd(ST7796S_PWCTR2);
    lcd_write_data8(0x06);

    /* Power Control 3 */
    lcd_write_cmd(ST7796S_PWCTR3);
    lcd_write_data8(0xA7);

    /* VCOM Control */
    lcd_write_cmd(ST7796S_VMCTR1);
    lcd_write_data8(0x18);

    /* Positive Voltage Gamma Control */
    lcd_write_cmd(ST7796S_GMCTRP1);
    lcd_write_data8(0xF0);
    lcd_write_data8(0x09);
    lcd_write_data8(0x0B);
    lcd_write_data8(0x06);
    lcd_write_data8(0x04);
    lcd_write_data8(0x15);
    lcd_write_data8(0x2F);
    lcd_write_data8(0x54);
    lcd_write_data8(0x42);
    lcd_write_data8(0x3C);
    lcd_write_data8(0x17);
    lcd_write_data8(0x14);
    lcd_write_data8(0x18);
    lcd_write_data8(0x1B);

    /* Negative Voltage Gamma Control */
    lcd_write_cmd(ST7796S_GMCTRN1);
    lcd_write_data8(0xF0);
    lcd_write_data8(0x09);
    lcd_write_data8(0x0B);
    lcd_write_data8(0x06);
    lcd_write_data8(0x04);
    lcd_write_data8(0x03);
    lcd_write_data8(0x2D);
    lcd_write_data8(0x43);
    lcd_write_data8(0x42);
    lcd_write_data8(0x3B);
    lcd_write_data8(0x16);
    lcd_write_data8(0x14);
    lcd_write_data8(0x17);
    lcd_write_data8(0x1B);

    /* 锁定命令集 */
    lcd_write_cmd(0xF0);
    lcd_write_data8(0x3C);
    lcd_write_cmd(0xF0);
    lcd_write_data8(0x69);

    uapi_tcxo_delay_ms(120);

    /* Display ON */
    lcd_write_cmd(ST7796S_DISPON);
}

errcode_t st7796s_init(void)
{
    /* 初始化GPIO */
    uapi_gpio_init();

    /* 初始化SPI引脚 */
    lcd_spi_init_pin();

    /* 初始化SPI主机 */
    lcd_spi_init_config();

    /* 执行LCD初始化序列 */
    lcd_init_sequence();

    /* 开启背光 */
    st7796s_set_backlight(true);

    osal_printk("ST7796S LCD init success.\r\n");
    return ERRCODE_SUCC;
}

